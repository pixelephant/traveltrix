$("document").ready(function(){
	

	$("#points ol li").click(function(){
		var $this = $(this);
		var id = $this.data("text");
		$this.addClass("active").siblings().removeClass("active");
		$("#text ul").scrollTo($("#"+id),300);
	});
	
	
	$("#slider a.nav").toggle(function(){
		$(this).removeClass("nextSlide").addClass("prevSlide");
	},function(){
		$(this).removeClass("prevSlide").addClass("nextSlide");
	});
	
	$('#main').serialScroll({
			target:'#slides',
			items:'.slide', // Selector to the items ( relative to the matched elements, '#sections' in this case )
			prev:'a.nav',// Selector to the 'prev' button (absolute!, meaning it's relative to the document)
			next:'img.next',// Selector to the 'next' button (absolute too)
			axis:'xy',// The default is 'y' scroll on both ways
			navigation:'#navigation li a',
			duration:700,// Length of the animation (if you scroll 2 axes and use queue, then each axis take half this time)
			force:true, // Force a scroll to the element specified by 'start' (some browsers don't reset on refreshes)

			onBefore:function( e, elem, $pane, $items, pos ){
				/**
				 * 'this' is the triggered element 
				 * e is the event object
				 * elem is the element we'll be scrolling to
				 * $pane is the element being scrolled
				 * $items is the items collection at this moment
				 * pos is the position of elem in the collection
				 * if it returns false, the event will be ignored
				 */
				 //those arguments with a $ are jqueryfied, elem isn't.
				e.preventDefault();
				console.log(pos);
				if(pos != 0){
					$("#top-layer").fadeOut();
					$("#basic-form").animate({
						top: 0
					});
					$("#form-wrap").height("+=60");
				}
				else{
					$("#top-layer").fadeIn();
					$("#basic-form").animate({
						top: "-60px"
					});
					$("#form-wrap").height("-=60");
				}
			},
			onAfter:function( elem ){
				//'this' is the element being scrolled ($pane) not jqueryfied
			}
		});
		
		$("#advanced-button").click(function(){
			$("#search-options").toggle();
		});
		
		$("#from,#to").datepicker();
		
		VideoJS.setupAllWhenReady();
	
		
});